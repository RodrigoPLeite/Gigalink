    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            Statement st = conecta.con.createStatement();
            st.executeUpdate("UPDATE produto SET id = '"+idProd.getText()+"', nome = '"+nomeProd.getText()+"', descricao = '"+descProd.getText()+"', valor = '"+valorProduto.getText()+"', idFornecedor = '"+idFornecedor.getText()+"' WHERE id = " + idProd.getText());
            JOptionPane.showMessageDialog(null, "Atualizado com Sucesso");
            idProd.setEditable(false);
        } catch (SQLException ex) {
            Logger.getLogger(TelaViewFornecedor.class.getName()).log(Level.SEVERE, null, ex);

        }    
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       try {

            Statement st = conecta.con.createStatement();
            st.executeUpdate("DELETE FROM produto WHERE id = " + idProd.getText());
            nomeProd.setText("");
            descProd.setText("");
            valorProduto.setText("");
            idFornecedor.setText("");
            idProd.setText("");
            idProd.setEditable(true);
            JOptionPane.showMessageDialog(null, "Excluído com Sucesso");

        } catch (SQLException ex) {
            Logger.getLogger(TelaViewFornecedor.class.getName()).log(Level.SEVERE, null, ex);

        } 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnTodosRegistrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTodosRegistrosActionPerformed
          try {

            Statement st = conecta.con.createStatement();
            ResultSet rs = st.executeQuery("Select id from produto");
            ArrayList todosRegistros = new ArrayList();
            while(rs.next()){
                todosRegistros.add(rs.getString("id"));
               lblTodosRegistros.setText("ID: " + (todosRegistros).toString().replace("[", "").replace("]", ""));
            }
         }
         catch (SQLException ex) {
            Logger.getLogger(TelaViewFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTodosRegistrosActionPerformed

