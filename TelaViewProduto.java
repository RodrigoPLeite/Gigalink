    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if(idFornecedor.getText().isEmpty() || idFornecedor.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Fornecedor não Cadastrado.\nFavor cadastrar.");
            TelaViewFornecedor tvf = new TelaViewFornecedor();
            tvf.setVisible(true);
        }else
        try {

            Statement st = conecta.con.createStatement();
            st.executeUpdate("INSERT INTO produto values('"+0+"', '"+nomeProd.getText()+"', '"+descProd.getText()+"', '"+valorProduto.getText()+"', '"+idFornecedor.getText()+"')");
              JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso");
        } catch (SQLException ex) {
            Logger.getLogger(TelaViewFornecedor.class.getName()).log(Level.SEVERE, null, ex);

        }                       
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
         try {

            Statement st = conecta.con.createStatement();
            ResultSet rs = st.executeQuery("Select * from produto where id = " + idProd.getText());
            if(rs.next()){
                nomeProd.setText(rs.getString("nome"));
                descProd.setText(rs.getString("descricao"));
                valorProduto.setText(rs.getString("valor"));
                idFornecedor.setText(rs.getString("idFornecedor"));
               idProd.setEditable(false);
            }
         }
         catch (SQLException ex) {
            Logger.getLogger(TelaViewFornecedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

